void main() {
  // Integer data type (int)
  int age = 25; // Represents a whole number without decimal points
  print('Age: $age');

  // Double data type (double)
  double height = 5.9; // Represents a number with decimal points
  print('Height: $height');

  // String data type (String)
  String name = 'Samuel Mboka'; // Represents a sequence of characters
  print('Name: $name');

  // List data type (List)
  List<int> numbers = [1, 2, 3, 4, 5]; // Represents an ordered collection of elements
  print('Numbers: $numbers');

  // Map data type (Map)
  Map<String, dynamic> person = {
    'name': 'Samuel',
    'age': 30,
    'city': 'New York'
  }; // Represents a collection of key-value pairs
  print('Person: $person');
}
